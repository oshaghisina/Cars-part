<script>
import { ref, computed } from "vue";
import { useRouter } from "vue-router";
import { useAuthStore } from "../stores/auth";

export default {
  name: "NavBar",
  setup() {
    const authStore = useAuthStore();
    const searchQuery = ref("");
    const mobileMenuOpen = ref(false);
    const userName = ref(authStore.user?.username || "Admin User");

    const userInitials = computed(() => {
      return userName.value
        .split(" ")
        .map((name) => name[0])
        .join("")
        .toUpperCase();
    });

    const performSearch = () => {
      if (searchQuery.value.trim()) {
        // Emit search event to parent component
        console.log("Searching for:", searchQuery.value);
        // Implement global search functionality
        performGlobalSearch(searchQuery.value);
      }
    };

    const clearSearch = () => {
      searchQuery.value = "";
    };

    const performGlobalSearch = async (query) => {
      try {
        // Navigate to search results page with query parameter
        const router = useRouter();
        router.push({
          path: "/search",
          query: { q: query },
        });

        // Alternative: Open search modal or show results inline
        // emit('global-search', { query, results: searchResults })
      } catch (error) {
        console.error("Global search failed:", error);
      }
    };

    const logout = () => {
      authStore.logout();
      // Redirect to login page if needed
    };

    return {
      authStore,
      searchQuery,
      mobileMenuOpen,
      userName,
      userInitials,
      performSearch,
      clearSearch,
      logout,
    };
  },
};
</script>

